Whipple::Whipple()
{
  // Setup the root finder and the numerical integrator
  initRootFinder();
  initODESolver();
  // Default parameters
  setBenchmarkParameters();
  setBenchmarkState();
  
  // zero out all the z's
  for (int i = 0; i < Z_MAX; ++i)
    z[i] = 0.0;

  // zero out the input torques
  Trw = Tfw = Ts = 0.0;

  // Camera settings
  theta = M_PI / 4.0;
  phi = 0.0;
  d = 1.0;
  ctx = .35;
  cty = .35; 
  ctz = 0.0;
  
  // Constants
  evalConstants();
  eoms();
  computeOutputs();

  // Write data to file to define numpy data type so that plotting is easy
  writeRecord_dt();
 
} // constructor

